package br.com.perifacode.operators;

public class OperadoresAtribuicao {

    public static void main(String[] args){

        int x = 21;

        System.out.println("--- Operadores de Atribuição ---");

        System.out.println("x = 5");
        System.out.println(x = 5);
        System.out.println("----------------");

        System.out.println("x + 3");
        System.out.println(x + 3);
        System.out.println("----------------");

        System.out.println("x - 3");
        System.out.println(x - 3);
        System.out.println("----------------");

        System.out.println("x * 3");
        System.out.println(x * 3);
        System.out.println("----------------");

        System.out.println("x / 3");
        System.out.println(x / 3);
        System.out.println("----------------");

        System.out.println("x - 3");
        System.out.println(x - 3);
        System.out.println("----------------");

        System.out.println("x % 3");
        System.out.println(x % 3);
        System.out.println("----------------");

        System.out.println("x & 3");
        System.out.println(x & 3);
        System.out.println("----------------");

        System.out.println("x | 3");
        System.out.println(x | 3);
        System.out.println("----------------");

        System.out.println("x ^ 3");
        System.out.println(x ^ 3);
        System.out.println("----------------");

        System.out.println("x >> 3");
        System.out.println(x >> 3);
        System.out.println("----------------");

        System.out.println("x << 3");
        System.out.println(x << 3);
        System.out.println("----------------");

    }
}
